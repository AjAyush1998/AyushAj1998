import java.util.*;
class asmd
{
public static void main(String args[])
	{
	Scanner sc=new Scanner(System.in);
	System.out.println("Enter 2 numbers for +,-,*,/");
	System.out.println("f1=");
	int f1=sc.nextInt();
	System.out.println("f2=");
	int f2=sc.nextInt();
	System.out.println("f1+f2="+(f1+f2));
	System.out.println("f1-f2="+(f1-f2));
	System.out.println("f1*f2="+(f1*f2));
	System.out.println("f1/f2="+(f1/f2));
	}
}